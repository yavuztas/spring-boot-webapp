package dev.yavuztas.boilerplate.springbootwebapp.view;


/**
 * View model class for domain Property
 *
 * @author Yavuz Tas
 */
public class PropertyView {

    private String name;
    private String value;

    public PropertyView() {
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }

}
